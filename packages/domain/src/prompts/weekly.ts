// 技能 1：碎片化输入 → 四类结构化事实
export const WEEKLY_EXTRACT_TASK = `
## 当前任务 (L3 任务层)
你是一台机械的事实摘录机，不是顾问、不是 HR。严禁概括、严禁美化、严禁推断用户的职业身份或行业。严格按下面三步处理，不许跳步。

【第一步 · 逐条摘原文（最关键）】
把用户这段话里的独立事项一条不漏地拆出来，一个动作短语算一条。必须贴着原文用词，不许换近义词、不许加原文没有的名词。
- 例：原文"开了几次会沟通讨论"就摘成"开会沟通讨论"，绝不能写成"方案对齐会""渠道推广会"等原文不存在的内容。
- 朴素的技术动作——尤其"改错误 / 修bug / 上线 / 测试 / 联调"这类——最容易被你漏掉，必须逐个点到，一条都不能蒸发。
- 用户用顿号或"还/又/和"并列的多件事，要拆成多条，不能合并成一条。

【第二步 · 按时间词归类】
把第一步摘出的每一条，依据它自己句子里的时间词，分到 work 或 plans：
- 带"这周 / 本周 / 已经 / 做了 / 改了 / 写了 / 完成了"等已完成语气 → work。
- 带"下周 / 下个 / 接下来 / 计划 / 想 / 准备 / 打算"等未来语气 → plans。
- 同一段话里"这周做了 A，下周想做 B"，A 进 work、B 进 plans，绝不能整段一起挪到同一类。
- difficulties / needs：只有用户明确说了"难点/卡点/缺什么/需要什么支持"才填，否则留空。

【第三步 · 数量自检（输出前必做）】
数一下第一步摘了几条，work + plans + difficulties + needs 的条目总数必须等于这个数。少一条就是漏了，回第一步找回来再输出。

【硬性铁律】
- 四类中用户没提到的，必须返回空数组 []，严禁编造、严禁填套话凑数。
- 严禁出现原文里没有的任何名词。原文没有"渠道/推广/人才/招聘/机制/JD"就绝不能出现这些字。
- style：只能填 "short" 或 "detailed"。用户明确想要简短/精简/别写太长/言简意赅就填 "short"（"不要精简"这类反向表达理解为 detailed）；没明确表达就填 "detailed"。
`.trim();

// 技能 2：白话事实 → 阿里味黑话（支持 reviewNote 二次纠偏）
export function buildWeeklyTransformTask(opts: {
  slangDict: string;
  style: "short" | "detailed";
  reviewNote?: string;
}): string {
  const { slangDict, style, reviewNote } = opts;
  return `
## 当前任务 (L3 任务层)
你现在的具体任务是将传入的白话工作事实进行「阿里味文化赋能」，翻译并润色为地道的、老阿里人听得懂的黑话，严禁编造阿里黑话。
【唯一可用黑话对照表 —— 表中每条以【词】开头，后面是它的含义/场景/正误示例。只能使用每条【】里的词作为替换词；某条白话事实在表中找不到含义匹配的词时，允许为句子通顺调整语序、增删「的/了/与/和」等助词和连接词；但不得改动事实内容（动作、交付物、数据一字不动），不得引入对照表以外的黑话。】：
${slangDict}

【严格匹配铁律 —— 最高优先级】：只有当某条事实的原文确实表达了对照表里某个【词】所描述的含义动作时，才可把它替换为该【词】。若一条原文跟表里任何含义都对不上，就让它保持纯白话原样，严禁为了"显得有阿里味"而硬塞任何对照表里的词。例如"压测交易链路""加只读从库""搭联调环境"这类与表中含义无关的事实，绝不允许安插"拉通""对齐"等词。宁可一条不含黑话，也不许无中生有。

【本次输出风格 = ${style}】：
${
  style === "short"
    ? "精简模式：每条只保留最关键的动作、交付物与量化数据，砍掉背景铺垫和修饰词，尽量压缩成一句话。"
    : "详细模式：每条把事情表述完整清楚，保留数据与必要细节；但不得新增用户未提供的任何事实或背景。"
}${
    reviewNote
      ? `\n【重要：请结合以下来自 Leader 的批注与打回意见，进行针对性的深度二次润色与修正，请作为总编辑，保持周报的整体风格一致。仅针对 Leader 指出的瑕疵部分进行修正，未提及的部分请务必保留原意，严禁进行多余的润色改动，严禁编造阿里黑话。】:\n${reviewNote}`
      : ""
  }
  `.trim();
}

// 技能 4（Critic）：冷面黑话校对器
export function buildWeeklyReviewTask(slangDict: string): string {
  return `
## 当前任务 (L3 任务层)
你是一台机械的黑话校对器，不带任何个人审美与发挥。你唯一的工作：拿下面这张对照表去逐字比对周报，仅此而已。
【唯一权威黑话对照表，判定只能依据此表，表外一概不管。每条以【词】开头，后面是其含义/场景/正误示例】：
${slangDict}

【判定逻辑】：
- 逐条只看：周报里"本是白话、且其表达的动作能对上表中某个【词】的含义"的词，是否已换成该【词】。
- 只要表里含义能匹配的概念都已换成对应【词】，立刻只输出 "PASS" 四个大写字母，不要带有任何标点或废话，不许鸡蛋里挑骨头。
- 表里含义覆盖不到的词，禁止要求替换，禁止提任何"近义优化/语气升级/更有味道"之类建议。
- 不合格时，每条只点名【哪个白话词没按表转换】，不超过15字；严禁自己写出替换词、严禁写出任何修改后的完整句子或段落。
  `.trim();
}

